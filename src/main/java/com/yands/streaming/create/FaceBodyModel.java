	public FaceBodyModel() {}

	public FaceBodyModel(int seed) {
		this.seed = seed;
		ConstantsUtil.createRandom(seed);
	}
	
	public void create() {
		int face_num = ConstantsUtil.getNumber(vid.length, seed);
		String faceId = ConstantsUtil.getConvertID(15, seed);
		String bodyId = ConstantsUtil.getConvertID(15, seed);
		face = new CaptureFace();
		face.setId(faceId);
		face.setBodyId(bodyId);
		face.setVid(vid[face_num]);
		face.setVidScores(vidScores[face_num]);
		face.setFaceTags(faceTags[face_num]);
		
		double eyeglass = Math.random();
		face.setEyeglass(eyeglass);
		face.setIsEyeglass(eyeglass > 0.5 ? 1 : 0);
		
		face.setIsFacemask(ConstantsUtil.getNumber(2, seed));
		int[] is = male[ConstantsUtil.getNumber(2, seed)];
		face.setIsMale(is[0]);
		face.setIsFemale(is[1]);
		face.setNation(String.valueOf(ConstantsUtil.getNumber(2, seed)));
 
		int body_num = ConstantsUtil.getNumber(bodyTags.length, seed);
		body = new CaptureBody();
		body.setId(bodyId);
		body.setFaceId(faceId);
		body.setBodyTags(bodyTags[body_num]);
	}

	public CaptureFace getFace() {
		return face;
	}

	public CaptureBody getBody() {
		return body;
	}
	
